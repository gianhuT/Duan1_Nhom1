    void insert() {
        SanPham model = getForm();
        if(!Validate()){
            return;
        }else{
            try {
                dao.insert(model);
                this.fillTableSanPham();
                this.setForm(model);
                JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Thêm mới thất bại !");
                e.printStackTrace();
            }
        }
    }

    void fillTableSanPham() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachSP.getModel();
        model.setRowCount(0);
        try {
            SanPham sanpham = new SanPham();
            List<SanPham> list = dao.selectAll();
            for (SanPham sp : list) {
                Object[] row = {
                    sp.getMaSP(),
                    sp.getTenSP(),
                    sp.getSoLuongTonKho(),
                    sp.getDonGia(),
                    sp.getChatLieu(),
                    sp.getMauSac(),
                    sp.getKichCo(),
                    sp.getMaNCC(),
                    sp.getThuongHieu(),
                    sp.getHinhSP(),
                };
                model.addRow(row);
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu: "+e.getMessage());
            e.printStackTrace();
        }
    }
    void fillTablePhanLoai() {
        DefaultTableModel model = (DefaultTableModel) tblPhanLoaiGiay.getModel();
        model.setRowCount(0);
        try {
            SanPham sanpham = new SanPham();
            List<SanPham> list = dsdao.selectByDSSP(sanpham.getMaSP());
            for (SanPham sp : list) {
                Object[] row = {
                    sp.getMaSP(),
                    sp.getTenSP(),
                    sp.getThuongHieu(),
                    sp.getGhiChu()
                };
                model.addRow(row);
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu");
        }
    }
